    private void llenarComboFiltro(){
        cboBusquedaEspecifica.removeAllItems();
        cboBusquedaEspecifica.addItem("ISBN");
        cboBusquedaEspecifica.addItem("Titulo");
        cboBusquedaEspecifica.addItem("Autor");
    }
    private void llenarTable(){
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            //No poder editar en la tabla Libro
            @Override   
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        modeloTabla.addColumn("ISBN");
        modeloTabla.addColumn("Título");
        modeloTabla.addColumn("Autor");
        modeloTabla.addColumn("Categoría");
        modeloTabla.addColumn("Año");
        modeloTabla.addColumn("Descripción");
        modeloTabla.addColumn("Stock");
        modeloTabla.addColumn("Estado");
        tblLibro.setModel(modeloTabla);
    }
    private void mostrarLista(ArrayList<Libro> lista){
        DefaultTableModel modeloListar = (DefaultTableModel) tblLibro.getModel();
        modeloListar.setRowCount(0);
        for(Libro l : lista){
            Object[] fila = {
                l.getIsbn(),
                l.getTitulo(),
                l.getAutor(),
                l.getCategoria(),
                l.getAnioPublicacion(),
                l.getDescripcion(),
                l.getStock(),
                l.getEstadoLibro()
            };
            modeloListar.addRow(fila);
        }
    }
