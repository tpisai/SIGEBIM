    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tblLibro.getSelectedRow();
        if(fila == -1){
            JOptionPane.showMessageDialog(null,"Seleccione un libro");
            return;
        }
        int respuesta =
        JOptionPane.showConfirmDialog(null,"¿Desea eliminar el libro?","Confirmar",
                JOptionPane.YES_NO_OPTION);
        if(respuesta == JOptionPane.YES_OPTION){
            listaLibros.remove(fila);
            archivo.guardarLibros(listaLibros);
            mostrarLista(listaLibros);;
            limpiar();
            JOptionPane.showMessageDialog(null,"Libro eliminado");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarEspecificoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarEspecificoActionPerformed
        String tipoBusqueda = cboBusquedaEspecifica.getSelectedItem().toString();
        String texto = txtBuscar.getText().trim().toLowerCase();
        ArrayList<Libro> resultados = new ArrayList<>();
        for(Libro l : listaLibros){
            switch(tipoBusqueda){
                case "ISBN" -> {
                    if(l.getIsbn().toLowerCase().contains(texto)){
                        resultados.add(l);
                    }
                }
                case "Titulo" -> {
                    if(l.getTitulo().toLowerCase().contains(texto)){
                        resultados.add(l);
                    }
                }
                case "Autor" -> {
                    if(l.getAutor().getNombre().toLowerCase().contains(texto)){
                        resultados.add(l);
                    }
                }
            }
        }
        // loguea si la búsqueda fue por Título
        if (tipoBusqueda.equals("Titulo")) {
            log.registrarBusqueda("Titulo", texto, resultados.size());
        }
        mostrarLista(resultados);
        if(resultados.isEmpty()){
            JOptionPane.showMessageDialog(null,"No existe ningun Libro con ese formato de busqueda");
        }
    }//GEN-LAST:event_btnBuscarEspecificoActionPerformed

    private void btnBuscarCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCategoriaActionPerformed
        String categoria = cboCategoriaBusqueda.getSelectedItem().toString().trim();
        ArrayList<Libro> resultados = new ArrayList<>();
        for(Libro l : listaLibros){
            if(l.getCategoria().getNombre().equalsIgnoreCase(categoria)){
                resultados.add(l);
            }
        }
        mostrarLista(resultados);
        if(resultados.isEmpty()){
            JOptionPane.showMessageDialog(null,"No existen libros en esta categoría");
        }
    }//GEN-LAST:event_btnBuscarCategoriaActionPerformed

